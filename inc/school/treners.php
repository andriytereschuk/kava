	<script>
		$(window).resize(function(){
			init();
		});
		$(function(){
			$content = $('#slider-desctop').html();
			$('#slider-mobile').append($content);
			init();
	  });

    var init = function() {
    	var w = $(window).width();
    	if(w<992) {
				var s3 = new Swiper('#slider-mobile .slider-t2', {
					prevButton: '.icon-top-slider',
					nextButton: '.icon-down-slider',
					speed: 400,
					loop: true,
					onInit: function(s3) {
						$('.icon-top-slider').show();
					}
		    });
    	}

    	else {
		    var s1 = new Swiper('.slider-t1', {
	        direction: 'vertical',
	        slidesPerView: 4,
	        resistanceRatio: 0,
	        onTap: function(swiper, event) {
	        	s2.slideTo(s1.clickedIndex);
	        }
		    });

				var s2 = new Swiper('#slider-desctop .slider-t2', {
					prevButton: '.icon-top-slider',
					nextButton: '.icon-down-slider',
					direction: 'vertical',
					speed: 500,
					resistanceRatio: 0,
					onSlideChangeStart: function(s2) {
						s1.slideTo(s2.activeIndex);
						$('.slider-t1 .swiper-slide').removeClass('active');
						$('.slider-t1').find('.swiper-slide').eq(s2.activeIndex).addClass('active');
						$('.icon-top-slider').show();
						$('.icon-down-slider').show();

						if(s2.isBeginning) {
							$('.icon-top-slider').hide();
						}

						if(s2.isEnd) {
							$('.icon-down-slider').hide();
						}
					},
					onInit: function(s2) {
						if(s2.slides.length == 1) {
							$('.icon-down-slider').hide();
						}
					}
		    });
    	}
    }
	</script>
